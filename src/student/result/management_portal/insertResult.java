    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        
        String rollno = rollNo.getText();
        String dbms = Dbms.getText();
        String os = Os.getText();
        String ai = Ai.getText();
        String ml = Ml.getText();
        String maths = Maths.getText();
        
        
        try{
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Tushar@9273");
            Statement st = con.createStatement();
            
            ResultSet rs = st.executeQuery("select * from student where rollno = '"+rollno+"'");
            if(rs.next()){
                st.executeUpdate("insert into result values('"+rollno+"','"+dbms+"','"+os+"','"+ai+"','"+ml+"','"+maths+"')");
                JOptionPane.showMessageDialog(null,"Data Update Successfully !");
                setVisible(false);
                new insertResult().setVisible(true);
            }
            else{
                
                JOptionPane.showMessageDialog(null,"Roll Number does not exist!");
            }
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_saveBtnActionPerformed

