    private void saveBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_saveBtnActionPerformed
        // TODO add your handling code here:
        String coursename = (String)courseName.getSelectedItem();
        String branchname = (String)branchName.getSelectedItem();
        String rollno = rollNo.getText();
        String name = Name.getText();
        String gender = (String)Gender.getSelectedItem();
        
        try{
            
            Class.forName("com.mysql.jdbc.Driver");
            Connection con = DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","Tushar@9273");
            Statement st = con.createStatement();
            st.executeUpdate("insert into student values('"+rollno+"','"+coursename+"','"+branchname+"','"+name+"','"+gender+"')");
            JOptionPane.showMessageDialog(null,"Data Saved Successfully !");
            setVisible(false);
            new adminIndex().setVisible(true);
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e.toString());
        }
    }//GEN-LAST:event_saveBtnActionPerformed

